SELECT
  CAST('2009-09-10 14:15:22.123' AS TIMESTAMP) AS ACCOUNT_ID,
  18 AS ACCOUNT_NO,
  CAST(12.4 AS DECIMAL(10, 2)) AS BALANCE,
  CAST(NULL AS DECIMAL(10, 2)) AS BLOCKING,
  CAST(NULL AS DOUBLE) AS DBL_COL,
  CAST(NULL AS DATE) AS OPENED_AT,
  CAST(NULL AS TIME) AS OPENED_AT_TIME,
  CAST(NULL AS BOOLEAN) AS VALID,
  CAST(NULL AS VARCHAR(50)) AS PRODUCT_NAME,
  CAST(NULL AS VARBINARY) AS STUFF
